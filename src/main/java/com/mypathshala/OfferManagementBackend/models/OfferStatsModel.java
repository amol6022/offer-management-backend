package com.mypathshala.OfferManagementBackend.models;

public class OfferStatsModel {
	
	int getRequests;
	
	int views;
	
	int clicks;
	
	int uses;

	public int getGetRequests() {
		return getRequests;
	}

	public void setGetRequests(int getRequests) {
		this.getRequests = getRequests;
	}

	public int getViews() {
		return views;
	}

	public void setViews(int views) {
		this.views = views;
	}

	public int getClicks() {
		return clicks;
	}

	public void setClicks(int clicks) {
		this.clicks = clicks;
	}

	public int getUses() {
		return uses;
	}

	public void setUses(int uses) {
		this.uses = uses;
	}
	
	
	
}
